package com.zaptech.shoppingcart;

public class ItemModel {
	private String itemName,itemCategory,itemImage;
	private int  itemPurchasePrice,itemSellsPrice,itemQuantity;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemCategory() {
		return itemCategory;
	}
	public void setItemCategory(String itemCategory) {
		this.itemCategory = itemCategory;
	}
	public String getItemImage() {
		return itemImage;
	}
	public void setItemImage(String itemImage) {
		this.itemImage = itemImage;
	}
	public int getItemPurchasePrice() {
		return itemPurchasePrice;
	}
	public void setItemPurchasePrice(int itemPurchasePrice) {
		this.itemPurchasePrice = itemPurchasePrice;
	}
	public int getItemSellsPrice() {
		return itemSellsPrice;
	}
	public void setItemSellsPrice(int itemSellsPrice) {
		this.itemSellsPrice = itemSellsPrice;
	}
	public int getItemQuantity() {
		return itemQuantity;
	}
	public void setItemQuantity(int itemQuantity) {
		this.itemQuantity = itemQuantity;
	}

	
	

	
	
}
